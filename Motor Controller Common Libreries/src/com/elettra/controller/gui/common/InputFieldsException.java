package com.elettra.controller.gui.common;

public class InputFieldsException extends IllegalArgumentException
{

	public InputFieldsException()
  {
	  super();
	  
  }

	public InputFieldsException(String message, Throwable cause)
  {
	  super(message, cause);
	  
  }

	public InputFieldsException(String s)
  {
	  super(s);
	  
  }

	public InputFieldsException(Throwable cause)
  {
	  super(cause);
	  
  }

	/**
   * 
   */
  private static final long serialVersionUID = 4557894560266984767L;

}
